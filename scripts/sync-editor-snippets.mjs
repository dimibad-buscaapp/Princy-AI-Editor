/**
 * Sync real source files into editor snippets for Monaco display.
 * Run: node scripts/sync-editor-snippets.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const outFile = path.join(root, "apps/frontend/src/features/editor/editor-snippets.generated.ts");

const sources = {
  "chat.routes.ts": "services/agents/src/routes/chat.routes.ts",
  "ollama.client.ts": "packages/ai-client/src/ollama.client.ts"
};

const snippets = {};
for (const [name, rel] of Object.entries(sources)) {
  const full = path.join(root, rel);
  snippets[name] = fs.existsSync(full) ? fs.readFileSync(full, "utf8") : `// ${name} not found\n`;
}

snippets["swarm.service.ts"] = `export class SwarmService {
  async orchestrate(agents: string[]) {
    return { active: agents.length, successRate: 97.8, tasksToday: 898 };
  }
}`;
snippets["package.json"] = '{\n  "name": "princy-ai-editor",\n  "version": "0.3.0"\n}';
snippets["README.md"] = "# Princy AI Editor\n\nInterface neural para agentes autônomos.";
snippets[".env"] = "OLLAMA_BASE_URL=http://127.0.0.1:11434\nOLLAMA_CHAT_MODEL=qwen3:8b\n";
snippets["tsconfig.json"] = '{\n  "compilerOptions": {\n    "strict": true\n  }\n}';

const body = `// Auto-generated by scripts/sync-editor-snippets.mjs — do not edit manually
export const editorSnippetsGenerated: Record<string, string> = ${JSON.stringify(snippets, null, 2)};
`;

fs.writeFileSync(outFile, body);
console.log("Wrote", outFile);
